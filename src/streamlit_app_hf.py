@st.cache_resource(show_spinner=False)
def get_hf_gemma_multimodal():
    """Charge le mod√®le Gemma 3n depuis Hugging Face"""
    try:
        st.info("Chargement du mod√®le multimodal Gemma 3n depuis Hugging Face...")
        
        # Utiliser le mod√®le distant de Hugging Face
        model_name = "google/gemma-3n-e2b-it"
        
        pipe = pipeline(
            "image-text-to-text",
            model=model_name,
            device=-1,  # Forcer le CPU
            torch_dtype=torch.float32,
        )
        
        st.success(f"Mod√®le Gemma multimodal charg√© : {pipe}")
        return pipe
    except Exception as e:
        st.error(f"Erreur lors du chargement du mod√®le Gemma multimodal : {e}")
        st.error(traceback.format_exc())
        return None

def analyze_image_hf(image, prompt=""):
    """Analyse une image avec le mod√®le multimodal Gemma 3n distant"""
    if st.session_state.gemma_model is None:
        return "‚ùå Mod√®le non charg√©. Veuillez le charger dans les r√©glages."
    try:
        pipe = st.session_state.gemma_model
        messages = [
            {
                "role": "system",
                "content": [
                    {"type": "text", "text": "Tu es un assistant agricole expert en diagnostic de maladies de plantes. Donne des r√©ponses claires et structur√©es."}
                ]
            },
            {
                "role": "user",
                "content": [
                    {"type": "image", "image": image.convert("RGB")},
                    {"type": "text", "text": prompt or "D√©cris la maladie pr√©sente sur cette plante et donne des recommandations pratiques."}
                ]
            }
        ]
        
        # Param√®tres de g√©n√©ration am√©lior√©s
        result = pipe(
            text=messages, 
            max_new_tokens=400,
            do_sample=True,
            temperature=0.7,
            top_p=0.9,
            repetition_penalty=1.1,
            pad_token_id=pipe.tokenizer.eos_token_id
        )
        
        if isinstance(result, list) and "generated_text" in result[0]:
            response = result[0]["generated_text"]
            
            # Nettoyage de la r√©ponse
            if response and len(response.strip()) > 0:
                # V√©rifier si la r√©ponse contient d√©j√† des recommandations
                if "recommandation" not in response.lower() and "action" not in response.lower():
                    response += "\n\n**Recommandations ou actions urgentes :**\n‚Ä¢ Isolez la plante malade si possible\n‚Ä¢ Appliquez un traitement adapt√©\n‚Ä¢ Surveillez les autres plantes\n‚Ä¢ Consultez un expert si n√©cessaire"
                
                return response
            else:
                return "‚ùå Erreur : R√©ponse vide du mod√®le"
        
        return str(result)
    except Exception as e:
        return f"‚ùå Erreur lors de l'analyse d'image : {e}"

def analyze_text_hf(text):
    """Analyse un texte avec le mod√®le Gemma 3n distant"""
    if st.session_state.gemma_model is None:
        return "‚ùå Mod√®le non charg√©. Veuillez le charger dans les r√©glages."
    try:
        pipe = st.session_state.gemma_model
        messages = [
            {
                "role": "system",
                "content": [
                    {"type": "text", "text": "Tu es un assistant agricole expert. Donne des conseils pratiques et structur√©s."}
                ]
            },
            {
                "role": "user",
                "content": [
                    {"type": "text", "text": text}
                ]
            }
        ]
        
        result = pipe(
            text=messages,
            max_new_tokens=300,
            do_sample=True,
            temperature=0.7,
            top_p=0.9
        )
        
        if isinstance(result, list) and "generated_text" in result[0]:
            return result[0]["generated_text"]
        return str(result)
    except Exception as e:
        return f"‚ùå Erreur lors de l'analyse de texte : {e}"

