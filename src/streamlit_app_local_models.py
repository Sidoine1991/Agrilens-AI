# --- Fonction d'analyse d'image ---
def analyze_image_local(image, prompt=""):
    if st.session_state.gemma_model is None:
        return "‚ùå Mod√®le non charg√©. Veuillez le charger dans les r√©glages."
    try:
        pipe = st.session_state.gemma_model
        messages = [
            {
                "role": "system",
                "content": [
                    {"type": "text", "text": "Tu es un assistant agricole expert en diagnostic de maladies de plantes. Donne des r√©ponses claires et structur√©es."}
                ]
            },
            {
                "role": "user",
                "content": [
                    {"type": "image", "image": image.convert("RGB")},
                    {"type": "text", "text": prompt or "D√©cris la maladie pr√©sente sur cette plante et donne des recommandations pratiques."}
                ]
            }
        ]
        
        # Param√®tres de g√©n√©ration am√©lior√©s
        result = pipe(
            text=messages, 
            max_new_tokens=400,
            do_sample=True,
            temperature=0.7,
            top_p=0.9,
            repetition_penalty=1.1,
            pad_token_id=pipe.tokenizer.eos_token_id
        )
        
        if isinstance(result, list) and "generated_text" in result[0]:
            response = result[0]["generated_text"]
            
            # Nettoyage de la r√©ponse
            if response and len(response.strip()) > 0:
                # V√©rifier si la r√©ponse contient d√©j√† des recommandations
                if "recommandation" not in response.lower() and "action" not in response.lower():
                    response += "\n\n**Recommandations ou actions urgentes :**\n‚Ä¢ Isolez la plante malade si possible\n‚Ä¢ Appliquez un traitement adapt√©\n‚Ä¢ Surveillez les autres plantes\n‚Ä¢ Consultez un expert si n√©cessaire"
                
                return response
            else:
                return "‚ùå Erreur : R√©ponse vide du mod√®le"
        
        return str(result)
    except Exception as e:
        return f"‚ùå Erreur lors de l'analyse d'image : {e}"

