# Fonction pour analyser le texte avec le mod√®le local Gemma
def analyze_text_local(prompt):
    """Analyse le texte avec le mod√®le local Gemma"""
    try:
        generator = get_local_gemma()
        result = generator(prompt, max_new_tokens=200)
        response = result[0]['generated_text']
        return response
    except Exception as e:
        return f"‚ùå Erreur d'analyse locale : {e}"

# Fonction pour appeler l'API Hugging Face (fallback)
def call_api_fallback(prompt, token=None):
    """Appelle l'API Hugging Face comme fallback"""
    try:
        url = "https://api-inference.huggingface.co/models/microsoft/DialoGPT-medium"
        
        headers = {"Content-Type": "application/json"}
        if token and token.strip():
            headers["Authorization"] = f"Bearer {token}"
        
        payload = {
            "inputs": prompt,
            "parameters": {
                "max_new_tokens": 200,
                "temperature": 0.7,
                "do_sample": True
            }
        }
        
        response = requests.post(url, headers=headers, json=payload, timeout=30)
        
        if response.status_code == 200:
            result = response.json()
            if isinstance(result, list) and len(result) > 0:
                return result[0].get("generated_text", "")
            return str(result)
        else:
            return None
            
    except Exception as e:
        return None

